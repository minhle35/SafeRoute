"""
PII detection accuracy benchmark — two run modes:

  pytest benchmarks/test_pii_accuracy.py -v
      Runs each corpus entry as an individual test.  Failures identify specific
      false-positives and false-negatives without aggregation.

  python benchmarks/test_pii_accuracy.py
      Aggregates TP/FP/FN/TN per entity type, computes precision/recall/F1,
      and writes benchmarks/ACCURACY_REPORT.md.

Both modes call _redact_text() directly — no FastAPI, no HTTP, no mocking.
The function is the correct abstraction boundary: it runs both regex and SpaCy
passes and returns RedactionResult(text, redacted_types, pii_detected).
"""

from __future__ import annotations

import sys
from dataclasses import dataclass
from pathlib import Path

# Ensure project root is on sys.path so both
#   `python benchmarks/test_pii_accuracy.py` and
#   `uv run pytest benchmarks/`
# can resolve vicroads_guardrails and benchmarks packages.
_ROOT = Path(__file__).resolve().parent.parent
if str(_ROOT) not in sys.path:
    sys.path.insert(0, str(_ROOT))

import pytest

from benchmarks.pii_corpus import CORPUS, CorpusEntry
from vicroads_guardrails.redactor import _redact_text


# ── Metrics dataclass ──────────────────────────────────────────────────────────

@dataclass
class EntityMetrics:
    entity_tag: str
    tp: int = 0
    fp: int = 0
    fn: int = 0
    tn: int = 0

    @property
    def precision(self) -> float | None:
        denom = self.tp + self.fp
        return self.tp / denom if denom > 0 else None

    @property
    def recall(self) -> float | None:
        denom = self.tp + self.fn
        return self.tp / denom if denom > 0 else None

    @property
    def f1(self) -> float | None:
        p, r = self.precision, self.recall
        if p is None or r is None or (p + r) == 0:
            return None
        return 2 * p * r / (p + r)


# ── pytest mode ───────────────────────────────────────────────────────────────
# Each corpus entry becomes a named test case.
# Failures reveal exactly which text / entity pair is broken.
# Known gaps (documented in pii_corpus.py comments) will appear as FAILED —
# that is intentional: the benchmark is an honest measurement, not a green suite.

@pytest.mark.parametrize("entry", CORPUS, ids=lambda e: e.label)
def test_corpus_entry(entry: CorpusEntry) -> None:
    result = _redact_text(entry.text)
    detected = entry.entity_tag in result.redacted_types
    if entry.is_positive:
        assert detected, (
            f"FN — PII not detected: "
            f"text={entry.text!r}, expected tag={entry.entity_tag!r}"
        )
    else:
        assert not detected, (
            f"FP — unexpected detection: "
            f"text={entry.text!r}, tag={entry.entity_tag!r}"
        )


# ── Report-generation mode ────────────────────────────────────────────────────

def compute_metrics() -> list[EntityMetrics]:
    """Run every corpus entry through _redact_text() and accumulate TP/FP/FN/TN."""
    by_tag: dict[str, EntityMetrics] = {}
    for entry in CORPUS:
        if entry.entity_tag not in by_tag:
            by_tag[entry.entity_tag] = EntityMetrics(entity_tag=entry.entity_tag)
        m = by_tag[entry.entity_tag]
        result = _redact_text(entry.text)
        detected = entry.entity_tag in result.redacted_types
        if entry.is_positive and detected:
            m.tp += 1
        elif entry.is_positive and not detected:
            m.fn += 1
        elif not entry.is_positive and detected:
            m.fp += 1
        else:
            m.tn += 1
    return list(by_tag.values())


def _fmt(val: float | None) -> str:
    return f"{val:.3f}" if val is not None else " N/A "


def generate_report(metrics: list[EntityMetrics]) -> str:
    total_entries = len(CORPUS)
    total_tp = sum(m.tp for m in metrics)
    total_fp = sum(m.fp for m in metrics)
    total_fn = sum(m.fn for m in metrics)
    total_tn = sum(m.tn for m in metrics)

    lines = [
        "# PII Detection Accuracy Report",
        "",
        "Generated by `python benchmarks/test_pii_accuracy.py`.",
        "Re-run the script after any change to `patterns.py` or `redactor.py`.",
        "",
        "## Results by entity type",
        "",
        "| Entity type | TP | FP | FN | TN | Precision | Recall | F1 |",
        "|---|---|---|---|---|---|---|---|",
    ]
    for m in metrics:
        lines.append(
            f"| {m.entity_tag} "
            f"| {m.tp} | {m.fp} | {m.fn} | {m.tn} "
            f"| {_fmt(m.precision)} | {_fmt(m.recall)} | {_fmt(m.f1)} |"
        )
    lines += [
        "",
        f"**Corpus totals** — "
        f"TP: {total_tp} | FP: {total_fp} | FN: {total_fn} | TN: {total_tn} "
        f"| entries: {total_entries}",
        "",
        "## Metric definitions",
        "",
        "| Metric | Formula | Meaning |",
        "|---|---|---|",
        "| Precision | TP / (TP + FP) | Of all detections, what fraction was correct? |",
        "| Recall | TP / (TP + FN) | Of all real PII, what fraction was caught? |",
        "| F1 | 2·P·R / (P+R) | Harmonic mean — penalises imbalance between P and R |",
        "",
        "## Known gaps",
        "",
        "- **VIC_DL** — word boundary `\\b` fires between a hyphen separator and the "
        "first digit, so `REF-123456789` is a false-positive (FP). "
        "Fix: add negative lookbehind `(?<!-)` to the pattern.",
        "- **VIC_PLATE / uppercase abbreviations** — `[A-Z0-9]{2,3}[- ]?[A-Z0-9]{2,3}` "
        "is intentionally broad to cover both standard and custom plate formats. "
        "Common uppercase word pairs (`NO GO`, `AI CD`) trigger false-positives. "
        "Context filtering (require a preceding `plate`/`rego` keyword) would reduce FPs "
        "at the cost of recall on bare-plate references in audit logs.",
        "- **VIC_PLATE / short digit strings** — `3000` matches as `30` (2-digit first group) "
        "+ empty separator + `00` (2-digit second group), producing a FP on postcodes. "
        "Requiring at least one alphabetic character in each group would eliminate this.",
        "- **SpaCy ORG / AU-specific organisations** — `en_core_web_sm` misses single-token "
        "Australian organisation names (`VicRoads`, `RACV`) that are absent from its "
        "training corpus. These appear as false-negatives (FN). "
        "Upgrade path: `en_core_web_lg` or a custom NER component.",
        "- **SpaCy LOCATION / regional cities** — smaller Victorian cities (e.g. `Ballarat`) "
        "are not reliably tagged as GPE by the small model. FN for location entities "
        "that aren't state capitals or internationally prominent.",
        "- **SpaCy DATE / dd/mm/yyyy format** — `01/06/2025` is not detected as a DATE "
        "by `en_core_web_sm` (model trained predominantly on US-format dates). "
        "Written-form dates (`15 January 1985`, `March 2023`) are correctly detected.",
        "- **DATE / bare digits** — bare all-digit strings (e.g. `20240115`) are "
        "intentionally skipped by the `isdigit()` guard in `redactor.py` to avoid "
        "false-positives on numeric IDs. This trades recall on compact date formats "
        "for precision on 8-digit reference numbers.",
    ]
    return "\n".join(lines) + "\n"


if __name__ == "__main__":
    print("Running PII accuracy benchmark …\n")
    metrics = compute_metrics()
    report = generate_report(metrics)
    print(report)
    out = Path(__file__).parent / "ACCURACY_REPORT.md"
    out.write_text(report, encoding="utf-8")
    print(f"Report written to {out}")
